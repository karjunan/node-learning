function multipleCase(input) {

    switch(input) {
        case 'red' :
            console.log("Red color"); 
        case 'blue' :
            console.log("Blue color");
        case 'purple':
            break;
        case 'green':
            console.log("green");
            break;       
    }
}

let color ='red';
multipleCase(color)